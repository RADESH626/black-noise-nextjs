// Función para obtener diseños por categoría
export const getMockDesignsByCategoria = (categoria) => {
  return mockDesigns.filter(design => design.categoria === categoria);
};

// Función para obtener diseños por usuario
export const getMockDesignsByUsuario = (usuarioId) => {
  return mockDesigns.filter(design => design.usuarioId === usuarioId);
};

// Función para obtener diseños públicos
export const getMockDesignsPublicos = () => {
  return mockDesigns.filter(design => design.estadoDesing === "PUBLICO");
};

// Función para obtener diseños por ID
export const getMockDesignById = (id) => {
  return mockDesigns.find(design => design._id === id);
};

// Función para obtener diseños más populares (por likes)
export const getMockDesignsPopulares = (limit = 5) => {
  return mockDesigns
    .filter(design => design.estadoDesing === "PUBLICO")
    .sort((a, b) => b.likes - a.likes)
    .slice(0, limit);
};

// Función para buscar diseños por palabras clave
export const searchMockDesigns = (query) => {
  const searchTerm = query.toLowerCase();
  return mockDesigns.filter(design => 
    design.nombreDesing.toLowerCase().includes(searchTerm) ||
    design.palabrasClave.some(keyword => keyword.toLowerCase().includes(searchTerm))
  );
};
